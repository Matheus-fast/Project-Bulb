function changeBackgroundDark() {

    var body = document.body;
    body.style.backgroundColor = '#5c2a2b';
    document.getElementById('bulbOff').src='./img/bulbOn.png';

}

function changeBackgroundWhite() {
    
    var body = document.body;
    body.style.backgroundColor = '#f7eee9';
    document.getElementById('bulbOff').src='./img/bulbOff.png';
}
